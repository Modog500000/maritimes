package xyz.volcanobay.maritimes.systems.input;

public class Bounds {
    public float left = 0;
    public float right = 0;
    public float bottom = 0;
    public float top = 0;

    public Bounds(float left, float right, float bottom, float top) {
        this.left = left;
        this.right = right;
        this.bottom = bottom;
        this.top = top;
    }
}
